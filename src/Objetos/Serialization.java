package Objetos;

public class Serialization {
	
	private String Db;
	private String User;
	private String Pass;
	private String Service;
	private int tpDB;
	
	
	public String getDb() {
		return Db;
	}
	public void setDb(String db) {
		Db = db;
	}
	public String getUser() {
		return User;
	}
	public void setUser(String user) {
		User = user;
	}
	public String getPass() {
		return Pass;
	}
	public void setPass(String pass) {
		Pass = pass;
	}
	public String getService() {
		return Service;
	}
	public void setService(String service) {
		Service = service;
	}
	public int getTpDB() {
		return tpDB;
	}
	public void setTpDB(int tpDB) {
		this.tpDB = tpDB;
	}

	
}
